"""Вкладка «Обзор»."""

import streamlit as st


def render(tab) -> None:
    with tab:
        st.markdown("### Обзор")
        with st.expander("Как пользоваться", expanded=False):
            st.markdown("""
1. Обзор — список модулей: веб-безопасность, расширения, CTF, стеганография, достоверность, детекция изображений.
2. Переключайте вкладки выше или ссылки внизу страницы.
3. Веб-безопасность — полный OWASP-скан или отдельные проверки.
4. CTF Hub — инструменты по категориям (Web, Pwn, Crypto, Forensics, Misc).
5. Экспорт — скачать последний скан или трекер флагов в JSON/HTML.
            """)
        col1, col2, col3, col4 = st.columns(4)
        col1.metric("Веб-сканер", "Активен", "OWASP Top 10")
        col2.metric("Расширения", "Активен", "CRX")
        col3.metric("Достоверность", "Активен", "Оценка текста")
        col4.metric("Изображения", "Активен", "Детекция")
        st.divider()
        c1, c2 = st.columns(2)
        with c1:
            st.markdown("#### Безопасность и CTF")
            st.success("**Веб-безопасность** — SQLi, XSS, CSRF, SSRF.")
            st.success("**Расширения** — статический анализ .crx/.zip.")
            st.success("**CTF Скан** — профили, поиск флагов, эксплуатация.")
            st.success("**Поиск флагов** — HTML, JS, cookies, заголовки.")
            st.success("**Разведка** — поддомены, технологии, dorks.")
            st.success("**Эксплойт** — PoC SQLi/XSS/LFI.")
        with c2:
            st.markdown("#### Анализ")
            st.info("**Достоверность текста** — оценка по шкале 1–10.")
            st.info("**Детекция изображений** — синтетика или реальное фото.")
            st.info("**AI Агент** — по запросу выполняет скан, поиск флагов, разведку.")
